const REQUIRED_KEYS = ['usr', 'eme', 'psw', 'age', 'loc', 'fll']
const users = input
.split('\n\n')
.map(user=> 
  Object.fromEntries(user
    .split('\n')
    .map(user => user.split(' '))
    .flat()
    .map(user => user.split(':')))
)

console.log(users)

let  validCount = 0
let  validUser = ''

for (const user of users) {
  let valid = true
  for (const key of REQUIRED_KEYS) {
    if (!(key in user)) {
      valid = false
    }
  }
  if (valid) {
    validCount++
    validUser = user.usr
  }
}

